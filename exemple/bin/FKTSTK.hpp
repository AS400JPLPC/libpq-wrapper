/// check row key
	bool PG_FKTSTK::check(   Zchar k_KTCGIM , Zdcml k_KTCDPO , Zchar k_KTCSTD)
{
try{

	requete= slc.prepare("SELECT count(*) FROM FKTSTK \
	 WHERE (KTCGIM = '?'  and KTCDPO = ? and KTCSTD = '?'  )",
		k_KTCGIM ,k_KTCDPO ,k_KTCSTD  ); 
 
	if ( slc.countqry(requete) > 0 )	return true ;
	return false ;
 
}catch (const std::exception& e)
	{
	  std::cerr<<e.what()<<"Check requete impossible"<<std::endl;
	  return false;
	}
} 



 		 



 		 



 		 
